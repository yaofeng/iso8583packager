    public static byte[] getMessage(final int id, final boolean withHead) {
        if (withHead) {
            final byte[] messageWithHeader = new byte[2 + 11 + MSG[id].length];
            byte[] messageLengthHeader = new byte[2];
            for (int i = 0; i < 2; i++) {
                int j = messageWithHeader.length - 2 >> (2 - 1 - i) * 8;
                messageLengthHeader[i] = (byte) (j & 0xff);
            }//for
            System.arraycopy(messageLengthHeader, 0, messageWithHeader, 0, 2);
            System.arraycopy(new byte[] { '>', 'T', 'P', 'D', 'U', // TPDU
                    'H', 'E', 'A', 'D', 'E', 'R',// HEADER
            }, 0, messageWithHeader, 2, 11);
            System.arraycopy(MSG[id], 0, messageWithHeader, 13, MSG[id].length);
            return messageWithHeader;
        } else {
            return MSG[id];
        }//if
    }// end of :public static byte[] getMessage
